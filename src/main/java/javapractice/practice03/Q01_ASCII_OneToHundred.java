package javapractice.practice03;

public class Q01_ASCII_OneToHundred {
    public static void main(String[] args) {

        /*

               Print numbers from 1 to 100 in the console.
               BUT! Do not use any numbers while writing the code.

         */
        for (int i = 'A'/'A'; i < 'e'; i++) {
            System.out.println(i);
        }

    }
}